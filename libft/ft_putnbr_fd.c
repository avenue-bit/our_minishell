#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	new_nbr;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		n *= -1;
		write(fd, "-", 1);
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	new_nbr = (n % 10) + 48;
	write(fd, &new_nbr, 1);
}
